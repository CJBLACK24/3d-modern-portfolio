export const calculateSizes = (isSmall, isMobile, isTablet) => {
  const defaultValues = {
    deskScale: 0.065,
    deskPosition: [0.25, -5.5, 0],
    cubePosition: [9, -5.5, 0],
    reactLogoPosition: [12, 3, 0],
    ringPosition: [-24, 10, 0],
    targetPosition: [-13, -13, -10],
  };

  if (isSmall) {
    return {
      deskScale: 0.05,
      deskPosition: [0.5, -4.5, 0],
      cubePosition: [4, -5, 0],
      reactLogoPosition: [3, 4, 0],
      ringPosition: [-5, 7, 0],
      targetPosition: [-5, -10, -10],
    };
  } else if (isMobile) {
    return {
      deskScale: 0.06,
      deskPosition: [0.5, -4.5, 0],
      cubePosition: [5, -5, 0],
      reactLogoPosition: [5, 4, 0],
      ringPosition: [-10, 10, 0],
      targetPosition: [-9, -10, -10],
    };
  } else if (isTablet) {
    return {
      deskScale: 0.065,
      deskPosition: [0.25, -5.5, 0],
      cubePosition: [5, -5, 0],
      reactLogoPosition: [5, 4, 0],
      ringPosition: [-12, 10, 0],
      targetPosition: [-11, -7, -10],
    };
  } else {
    return defaultValues;
  }
};

